#include <stdio.h>

int main()
{
    int a[3][3] = {{1,2,3}, {4,5,6}, {7,8,9}};//声明二维数组并初始化
    int i, j, temp;

    int (*p)[3] = a;//声明数组指针，用于存放数组a的行指针
    int *q = a[0];

    //进行数组转置操作
    for (i = 0; i < 3; i++)    //遍历每一行
    {
        for(j = i + 1; j < 3; j++)
        {
            temp = *(*(p + j) + i);
            *(*(p + j) + i) = *(q + j);
            *(q + j) = temp;
        }

        q = q + i * 3;
    }

    //输出转置后的结果
    for (i = 0; i < 3; i++)
    {
        for (j = 0; j < 3; j++)
        {
            printf("%d ", a[i][j]);
        }

        printf("\n");
    }

    return 0;
}


